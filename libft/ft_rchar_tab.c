#include "includes/libft.h"

void	ft_rchar_tab(char **tab)
{
	int	i;
	int x;

	x = 0;
	i = 0;
	while (tab[i])
	{
		if (x != 0)
			ft_putstr("-");
		ft_putstr(tab[i]);
		++i;
		++x;
	}
	ft_putchar('\n');
}
